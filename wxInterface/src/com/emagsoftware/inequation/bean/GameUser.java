package com.emagsoftware.inequation.bean;

import java.util.Date;

public class GameUser {
	//主键
	private Integer id;
	
	//初始挑战次数
	private Integer ownTimes;
	
	//已挑战次数
	private Integer tryTimes;
	
	//挑战成功的次数
	private Integer successTimes;
	
	//可抽奖次数
	private Integer lotteryTimes;
	
	//已抽奖次数
	private Integer usedLotteryTimes;
	
	//创建时间
	private Date createTime;
	
	//微信用户表主键
	private String wxuserId;
	
	//用户最高分
	private String highScore;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getOwnTimes() {
		return ownTimes;
	}

	public void setOwnTimes(Integer ownTimes) {
		this.ownTimes = ownTimes;
	}

	public Integer getTryTimes() {
		return tryTimes;
	}

	public void setTryTimes(Integer tryTimes) {
		this.tryTimes = tryTimes;
	}

	public Integer getSuccessTimes() {
		return successTimes;
	}

	public void setSuccessTimes(Integer successTimes) {
		this.successTimes = successTimes;
	}

	public Integer getLotteryTimes() {
		return lotteryTimes;
	}

	public void setLotteryTimes(Integer lotteryTimes) {
		this.lotteryTimes = lotteryTimes;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}


	public Integer getUsedLotteryTimes() {
		return usedLotteryTimes;
	}

	public void setUsedLotteryTimes(Integer usedLotteryTimes) {
		this.usedLotteryTimes = usedLotteryTimes;
	}

	public String getWxuserId() {
		return wxuserId;
	}

	public void setWxuserId(String wxuserId) {
		this.wxuserId = wxuserId;
	}

	public String getHighScore() {
		return highScore;
	}

	public void setHighScore(String highScore) {
		this.highScore = highScore;
	}

}
